		<header class="subsection border">Rule Enforcement</header>
		<p>None of us like to see a guild member have to be kicked; it can cause frustration, animosity, and unnecessary drama and stress on everyone's life that's involved. During the event that one of the above rules is broken, we try our best to avoid outright kicking the member and try to find a peaceful solution instead.</p>
		<p>It varies on a case-by-case basis, but generally, we will warn the offending players of their wrongdoing. If it continues happening, we will repeat the warning more severely and threaten appropriate punishment, be it demotion or kicking.</p>
		<p>If the behavior continues, we will either demote or kick the player from the guild with ample warning. We will allow them to remove whatever they have contributed from the guild if need be. This includes resources, some formulas, tech components, or trophies stored in the structures <i>by them and them only</i></p>
		<p>After being demoted or kicked, it is possible to regain our trust and find your way back in the guild if you prove yourself to be truly regretful and never do the behavior again. However, the second time around is much less forgiving and any misstep may result in further demotion or another kick.</p>
		
		<header class="article border">Activity Policy</header>
		<p>While there is no hard date, the Overseers will generally run through the guild and purge members marked with 'Inactive' every 3 or 4 months or will demote members from their current rank to inactive if there has not been a single peep out of said member for months.</p>
		<p>Inactivity is not a banworthy offense or anything of the sort, but if someone is going to quit the game, we prefer to keep the guild clean of long-gone members.</li>
		<p>The process is as follows:</p>
		<ul>
			<li>If you are inactive for 3+ months, you will be demoted to Inanctive during the next sweep through the member list. You will remain in the Inactive rank until you log in again and get your rank updated.</li>
			<li>If you continue inactivity for another 3+ months and are in the Inactive role, you will be on the chopping block for the next purge event.</li>
			<li>Once you are kicked for inactivity, you are welcome back any time you return to the game. Let anyone know you're back and we'll send another invite and re-instate your prior rank.</li>
			<li>If you are inactive again after your rank is instated, the process starts over again.</li>
		</ul>
		<p>Exceptions:</p>
		<ul>
			<li>You have someone in the Preserver or Overseer ranks know beforehand of an extended absence. We don't penalize people for being able to sub or if IRL issues arise. We just like to know if something is coming up so we can keep it in mind during the next purge.</li>
			<li>You have more than one character in the guild and are not active on all of them. We check each individual character for activity, but if at least one character with your ID is active at the time of the purge, you are fine. This is the primary purpose of the points system - to save you from being accidentally kicked.</li>
		</ul>
