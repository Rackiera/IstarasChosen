		<header class="article border">Rules</header>
		<ol>
			<li>No powerleveling new players (alts of others or your own do not count). You will only be given <b>one</b> warning.</li>
			<li>If someone is harassing you, don't be afraid of reporting it to the Preservers and Overseers; we are here to protect you.</li>
			<li>If you are in the wrong, there is nothing wrong in apologizing; don't be mean to others.</li>
			<li>Anyone interested in joining should be familiar to the higher ranks and have no history of negative behavior.</li>
