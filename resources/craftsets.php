		<a name="biped"><header class="subsection border">Biped Craft Suits</header></a>
			<p>Bipeds are complicated in the fact that they have access to 39 craft skills, of which approximately 37 are used regularly in crafting. (The excluded two - Transmutation and Salvaging - are used, but do not require any boosting / are not used often.)</p>
			<p>While all crafting skills by bipeds are also affected by statistics just like dragons are, the sheer amount of sets, variations among the sets, lack of socketing, and general complexity makes it unfeasible to further min-max crafting sets by applying statistic techniques and crystals. Instead, it is overall better to focus on crafting as few sets as possible with as many techniques on one as possible.</p>
			<p>I have yet to not reach optimal on an item using a set techniqued in this sort of way, so these should help you reach optimal in anything you require as a GMC. I cannot, however, promise it will help you reach optimal without a class maxed out that gives the max skill per level you are looking for.</p>
			<p>The lack of crystals and statistics does not make biped gear any less complicated, however; in fact, it makes it harder to wrap your head around. If you simply want to know what to put on each set, please simply skip to <a href="#bipedarmorsets">the craft set subsection</a> and reference <a href="#settypes">Set Types</a> for which pieces you need per listed set type.</p>
			<p style="clear:both"><i><a href="#top">Return to Top <span style="font-size:.75em;">&#9650;</span></a></i></p>
		<a name="bipedintroduction"><header class="subsection2 border">Mechanics</header></a>
			<p>Unlike dragons, bipeds have more than 10 equipment slots between armor and jewelry. In order to counteract bipeds being able to have more of a boost from all of their armor than dragons can gain from their scales, biped armor and jewelry pieces are segregated into three 'subclasses' of the overall technique repertoire. What this means is that there are three combinations of armor pieces and jewelry that equate to ten pieces, each taking specific techniques and excluding some others.</p>
			<p>I refer to these as "set types". Each set type has a specific collection of skills assigned to it. To make things easier, I refer to each set type with a letter.</p>
			<p>Set Type "A" contains all of the refining skills; these are all of the skills that take materials and refine them into finalized products, or specialized products that are used in a final product. (Such as tinkering hinges used to make cargo disks)</p>
			<p>Set Type "B" contains all of the raw resource harvesting skills.</p>
			<p>Set Type "C" contains all of the processing skills; these are the skills involved in making raw resources into refined materials, such as metal bars or stone bricks.</p>
			<p>In the next subsection, I list the particular pieces of armor that the techniques in each set are able to be applied to.</p>
			<p>Note that you can further min-max your crafting sets by adding Benevolence techniques. These techniques have their own set of armor they can be applied to, and give a bonus of +10 to a selection of 3 skills or +15 to 2 skills. As the full sets listed above combined with Expert crafting stations has given me optimal on everything, they are not truly necessary for a GMC.</p>
			<p>Despite the lack of necessity, Benevolances still add extra skill to perhaps make you optimal on higher tier materials out in the field. Therefore, I have written two sections. The 'simple' biped armor is a combination of sets that make logical sense, but lack benevolences and therefore gives less overall skill. It is, however, simpler and has less armor pieces overall, saving on stack space.</p>
			<p>The 'advanced' armor is a combination of sets that utilizes benevolences to their furthest extent, giving the biggest bonus, but also taking up the most stack space.</p>
			<p>Finally, it is possible to min-max your armor even further by messing with mix-matching the Master Crafter's Cargo gear. Individual pieces can give up to 200 skill, allowing you to mix-match to get bonuses of multiple hundred very easily. You likely won't ever need this much skill, but it's fun to know regardless.</p>
