		<a name="intro"><header class="subsection border">Introduction</header></a>
			<p>In Istaria, dragons and bipeds have different - yet similar - ways of creating sets known as crafting scales or crafting armor. While simply slapping a technique on armor works, sometimes you need to really dig deep and min-max your skills to reach optimal in harder situations.</p>
			<p>In this guide, I delve into creating the most min-maxed sets possible with the current in-game resources. I generally list two versions - a simpler less optimized version and a complex optimized version. Whichever version you wish to use is up to personal choice.</p>
			<p>I've divided this document into two large sections - dragons and bipeds - since they differ so greatly. However, in each subsection, I've followed a pattern.</p>
			<p><b>Mechanics</b> is where I explain my reasoning for each set, as well as some general math involved in each section. This helps give the 'why' behind the 'how' of the armor. It's not necessary to know and can easily be skipped.</p>
			<p><b>Simple</b> sets are the less optimized, but more common and simple versions of crafting sets that get the job done.</p>
			<p><b>Advanced</b> sets are the most optimized and min-maxed sets that give the highest bonus, but are harder to make.</p>
			<p>In bipeds, the extra section <b>set types</b> lists the armor pieces that different sets of crafting techniques can be applied to for reference.</p>
			<p>I hope this guide comes across as informative and helpful to all your crazy crafters out there. It's intended for max level characters to max out their skills, but people leveling classes can refer to the 'simple' sections for creating craft gear for leveling up as well, with some modifications. (Bipeds can technically use the advanced version too, but that would be a lot of coin for something simple if you're crafting below T5.)</p>
		<a name="dragon"><header class="subsection border">Dragon Craft Scales</header></a>
			<p>In comparison to bipeds, as usual, dragons are very simple. Their ten scales can be techniqued with every crafting technique in any combination. The primary difference from bipeds, however, is that dragon scales have a statistical boost built into each scale. Since statistics play into boosting your skills, it is favorable to create more scale sets with the trade-off that each scale set has techniques on it that get boosted by the stastistic the scale is as well as any socketed crystals and benevolences.</p>
			<p>To see an explanation of which skills are boosted by which statistics, read on. If you are uninterested and simply want to see what the optimized combinations are, skip ahead to <a href="#dragonarmorsets">this section</a>.
		<a name="dragonintroduction"><header class="subsection2 border">Mechanics</header></a>
			<p>Dragons have access to 13 crafting techniques, reflecting their 13 skills. However, two are useless; transmutation (dragons will essentially never use this skill except for the Rite of Passage) and salvaging (you don't need salvaging to craft, only to deconstruct, and thus don't need to boost it). Each of these skills are affected by at least two of four statistics; Power, Focus, Strength, and Dexterity. The 'primary' statistic is what I use to refer to the statistic that affects each crafting skill the <i>most</i>. For example, Smelting is affected by a mix of 8% Strength and 2% Power. For every 1000 Strength you gain, you will gain 80 Smelting, while for every 1000 Power, you gain 20. Thus, Strength is its primary statistic.</p>
			<p>This information is used to determine what statistic the scales the skills are teched on should be. In the end, it adds only between 10 and 20 extra skill, but that is still skill you do not have that you could have. Only primary statistics play enough of a part to be considered seriously in most cases - once you start getting below 5%, the ~125 of a secondary attribute techniqued on your scales will only net you about 6 skill or less.</p>
			<p>To further min-max your skills, it is also favorable to utilize a combination of Priceless Scales and T5 statistic scales. Since that's not feasible for all people, I've included both versions below.</p>
			<!--<p>Beyond this, Benevolences play a much larger role on dragons than on bipeds. This is the primary reason why scale sets are split even further. While you could simply run Mining/Quarrying/Smelting on a Strength scaleset and still be accurate to the primary attribute, you will be giving up on 150 extra skill in Mining and Quarrying from Miner's Benevolence.</p>
			<p>Benevolences overall add more stats than a crystal would, in most situations. (A +65 Stat crystal adds approximately +4-7 skill, while the worst Benevolence adds +10.)</p>
			<p>Therefore, crafting skills are grouped - overall - by primary statistic first, then best Benevolence second.</p>-->
